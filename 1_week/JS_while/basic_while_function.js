//This code shows the basic code for while loop that counts from 0 to 10 and set it in a function

// 1. Set variable
let i = 0 // variable i is equal to 0

//2. Define function
function whileLoop(i){
    while (i <= 10){   //while i is less or equal to 10, execute:
        console.log(i) // this is the line that is going to be executed, it will print the value of i
        i++ // same as i = i + 1
    }
}

whileLoop(i)
