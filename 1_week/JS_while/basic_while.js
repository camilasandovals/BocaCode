//This code shows the basic code for while loop that counts from 0 to 10

// 1. Set variable
let i = 0 // variable i is equal to 0

//2. Define while loop, while i is less or equal to 10, execute:
while (i <= 10){ 
    console.log(i) // this is the line that is going to be executed, it will print the value of i
    i++ // same as i = i + 1
}