//This code shows the estructure of a multi array

const container = 
[
 [1, 2, 3], //row number 1
 [4, 5, 6], //row number 2
 [7, 8, 9, 10, 11] //row number 3 
]

console.log(container[0]) // prints first row [1,2,3]
console.log(container[1]) // prints second row [4,5,6]
console.log(container[0][0]) //prints first row first column [1]
console.log(container[1][0]) //prints second row first column [4]
console.log(container[2][4]) //prints second row first column [11]
