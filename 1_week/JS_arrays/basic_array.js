//This code shows the basic structure of an array 
const newArray = ['Camila', 26, 'Colombian']

console.log(newArray) //prints the full array
console.log(newArray[0]) //prints position number 0 of the array, which is 'Camila'
console.log(newArray[1]) //prints position number 1 of the array, which is 26
console.log("The nationality is " + newArray[2]) // prints "The nationality is Colombian"

//Let's change a value of the array 
newArray[0] = 'John' // We change the value stored in position 0 of newArray to 'John'


