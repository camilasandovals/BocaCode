// Declare a constant variable 'a' and assign it a value of 5
const a = 5

// Declare a constant variable 'b' and assign it a value of 4
const b = 4

// Declare a constant variable 'c' and assign it the product of 'a' and 'b'
const c = a * b 

// Output the value of 'c' to the console
console.log(c) 

// Declare a constant variable 'subtotal' and assign it a value of 159.87
const subtotal = 159.87

// Declare a constant variable 'taxRate' and assign it a value of 0.07
const taxRate = 0.07 

// Declare a constant variable 'taxTotal' and assign it the product of 'subtotal' and 'taxRate'
const taxTotal = subtotal * taxRate

// Declare a constant variable 'total' and assign it the sum of 'subtotal' and 'taxTotal'
const total = subtotal + taxTotal

// Output the string "Total: " followed by the value of 'total' to the console
console.log("Total: " , total)

